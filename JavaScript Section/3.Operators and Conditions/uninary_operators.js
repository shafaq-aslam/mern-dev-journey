let num1 = 26;
let num2 = 5;

// Unary Operators
console.log(num1--); // 26
console.log(num1); // 25
console.log(--num1); // 24
console.log(num1); // 24

console.log(num2++); // 5
console.log(num2); // 6
console.log(++num2); // 7
console.log(num2) // 7

console.log(num1--); // 26
console.log(++num1); // 26
console.log(num1) // 26
console.log(num1++) // 26
console.log(--num1); // 26
console.log(num1); // 26

console.log(++num2); // 6
console.log(--num2); // 5
console.log(num2--); // 5
console.log(num2); // 4
console.log(--num2); // 3
console.log(num2++); // 3
console.log(num2); // 4





